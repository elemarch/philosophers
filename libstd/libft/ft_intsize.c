int	ft_intsize(int n)
{
	int i;

	i = 0;
	if (n < 0)
	{
		if (n == -2147483648)
			n++;
		i++;
		n = -n;
	}
	while (n > 9)
	{
		n = n / 10;
		i++;
	}
	return (i + 1);
}
