#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n == INT32_MI)
	{
		ft_putstr("-2147483648");
		return ;
	}
	if (n < 0)
	{
		write(fd, "-", 1);
		n *= -1;
	}
	if (n < 10)
		ft_putchar_fd('0' + n, fd);
	else
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
