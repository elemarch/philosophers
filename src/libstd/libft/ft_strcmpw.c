#include "libft.h"

int	ft_strcmpw(char *s1, char *s2)
{
	int i;

	i = 0;
	if ((!s1 && !s2) || (s1 && !s2))
		return (1);
	if (!s1 || !s2)
		return (0);
	while (*s1)
	{
		if (s2[i] == '*')
		{
			while (s2[i + 1] == '*')
				i++;
			if (*s1 != s2[i + 1])
				s1++;
			else
				i++;
			continue ;
		}
		if (s2[i] == '\0' && *s1 != '\0')
			return (0);
		if (s2[i] == *s1++)
			i++;
	}
	return ((*s1 == '\0' && (s2[i] == '\0' || s2[i] == '*')) ? 1 : 0);
}
